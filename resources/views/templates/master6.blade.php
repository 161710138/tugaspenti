				<b>Pengertian Puisi <br></b>
Puisi merupakan salah satu ragam karya sastra yang terikat dengan irama, ritma, rima, bait, larik dan ditandai dengan bahasa yang padat. <br> Puisi juga merupakan seni tertulis yang mana menggunakan bahasa sebagai kualitas estetiknya atau keindahanya. Puisi dibedakan menjadi dua yakni puisi lama dan puisi baru.<br>

Puisi lama ialah puisi yang terikat dengan aturan-aturan tertentu. Aturan-aturan tersebut antara lain : Jumlah kata dalam satu baris; jumlah baris dalam satu bait; rima (persajakan ); banyaknya suku kata dalam setiap baris; dan irama.<br>

Puisi baru ialah puisi yang tidak terikat oleh aturan-aturan sehingga lebih bebas bentuknya daripada puisi lama, baik dalam segi jumlah suku kata, baris, ataupun sajaknya.<br>

Ciri-Ciri Puisi Lama
Ada beberapa ciri puisi lama, antara lain sebagai berikut:

Berupa puisi rakyat yang tidak diketahui nama pengarangnya.
Terikat oleh aturan-aturan seperti jumlah baris dalam setiap baitnya, sajak dan jumlah suku kata dalam setiap barisnya.
Disampaikan dari mulut ke mulut sehingga sering disebut juga dengan sastra lisan / kesusastraan lisan.
Menggunakan majas/gaya bahasa tetap (statis) dan klise.
Berisikan tentang kerajaan, fantastis dan istanasentris.
Jenis Puisi lama<br>

<b>Adapun jenis-jenis puisi lama antara lain:</b><br>

Mantra yakni ucapan-ucapan yang dianggap memiliki kekuatan ghaib.
Pantun merupakan puisi yang bersajak a-b-a-b, dimana pada tiap bait ada 4 baris, dalam tiap baris terdiri dari 8 -12 suku kata, dan 2 baris pertama sebagai sampiran dan 2 baris setelahnya sebagai isi.
Karmina yang merupakan pantun kilat seperti pantun tetapi lebih pendek
Gurindam ialah puisi dimana pada tiap bait terdiri dari 2 baris, bersajakkan a-a-a-a, dan berisikan nasihat.
Seloka yakni pantun berkait yang ditulis menggunakan bentuk syair atau pantun, bisa empat batis atau lebih.
Syair yang merupakan puisi dengan ciri-cirinya yakni pada tiap bait ada 4 baris, bersajakkan a-a-a-a, dan berisikan nasehat-nasehat atau cerita.
Talibun yaitu pantun genap dimana pada tiap bait terdiri dari 6/8/10 baris.
<br>
<b>Ciri-Ciri Puisi Baru</b><br>
Yang ini adalah ciri-ciri puisi baru, antara lain:


 
Diketahui nama pengarangnya
Perkembangannya secara lisan dan tertulis
Tidak terikat oleh aturan seperti jumlah baris, jumlah suku kata dan rima.
Menggunakan majas / gaya bahasa yang berubah-ubah (dinamis).
Pada umumnya berisikan tentang kehidupan
Biasanya lebih banyak menggunakan sajak pantun dan syair
Bentuknya lebih rapi dan simetris
memiliki rima akhir yang lebih teratur
Pada tiap-tiap baris berupa kesatuan sintaksis
Pengertian Puisi, Ciri-Ciri Dan Jenis Puisi Terlengkap

<b>Jenis Puisi Baru</b><br>

Berikut adalah jenis-jenis dari puisi baru:<br>

Balada yakni puisi yang berisikan sebuah cerita atau kisah.<br>
Himne yaitu puisi pujaan yang ditujukan untuk Tuhan, pahlawan dan tanah air.<br>
Ode ialah puisi yang berbentuk sanjungan untuk orang-orang yang berjasa. Menggunakan nada atau irama yang sangat resmi, membahas tentang sesuatu yang mulia, dan memiliki sifat yang menyanjung.<br>
Epigram merupakan puisi yang berisikan ajaran ataupun tuntunan.<br>
Romansa ialah puisi yang isinya tentang luapan perasaan cinta dan kasih sayang.<br>
Elegi yakni puisi tentang kesedihan.<br>
Satire ialah puisi yang isinya berupa sindiran ataupun kritikan.<br>
Distikon merupakan puisi dimana pada tiap baitnya terdiri dari 2 baris.<br>
Terzina ialah puisi dimana tiap baitnya terdiri atas 3 baris.<br>
Kuatrain yakni puisi empat seuntai dimana puisi yang tiap baitnya terdiri dari 4 baris. <br>
Kuint ialah puisi lima seuntai yang mana pada tiap baitnya terdiri dari 5 baris. <br>
Sektet yaitu puisi enam seuntai yang tiap baitnya terdiri dari 6 baris. <br>
Septime ialah puisi yang tiap baitnya terdiri atas tujuh baris atau  puisi tujuh seuntai.<br>
Oktaf/Stanza merupakan puisi dimana tiap baitnya terdiri dari 8 baris.<br>
Soneta ialah puisi yang terdiri dari 14 baris dan terbagi menjadi dua, yakni pada dua bait pertama masing-masing empat baris dan pada dua bait kedua masing-masing tiga baris.<br>
